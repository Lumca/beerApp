const colorPicker = (arg) => {
    if (arg === 1) {
        return '#e03636';
    } else if (arg === 2) {
        return '#52a852';
    } else if (arg === 3) {
        return '#5b5bfc';
    } else if (arg === 4) {
        return '#d964fc';
    } else if (arg === 5) {
        return '#5bfccc';
    } else if (arg === 6) {
        return '#2c6217';
    } else if (arg === 7) {
        return '#fa0162';
    } else if (arg === 8) {
        return '#6b94ce';
    } else if (arg === 9) {
        return '#d9d9ff';
    } else {
        return '#d5a930';
    }
}

export default colorPicker;